// principios importantes

// 1. abstração -> como traduzir um objeto do mundo real para dentro do sistema (informações pertinentes para o sistema)
// 2. encapsulamento -> detalhes de implementação (complexidade) escondidos, mostrando uma interface simples para interação com o objeto (facilita a adaptação a mudanças)
// 3. herança -> objetos que podem herdar propriedades e comportamentos de outros objetos 'é um'
// 4. polimorfismo -> um objeto poder ter várias formas, você tem um conceito concreto, e ele pode se transformar em outros que implementam esse conceito